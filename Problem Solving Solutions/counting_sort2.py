def countingSort(arr):
    # Write your code here    
    newArr = [0]*100
    finArr = list()
    for i in range(len(arr)):
        newArr[(arr[i])]+=1
    for j in range(len(newArr)):
        finArr+=[j]*newArr[j]
    return finArr 

